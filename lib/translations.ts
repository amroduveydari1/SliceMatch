export function getItemTranslation(itemId: string, lang: Language) {
  const key = itemKeyMap[itemId];
  if (!key) return { name: itemId, desc: '' };
  return translations[lang].items[key];
}

export function getReasonTranslation(reason: string, lang: Language) {
  const t = translations[lang].reasons;
  const reasonMap: Record<string, string> = {
    'light portion': t.lightPortion,
    'hearty size': t.heartySize,
    'satisfying flavors': t.satisfyingFlavors,
    'fresh and light': t.freshAndLight,
    'bold heat': t.boldHeat,
    'timeless choice': t.timelessChoice,
    'unique twist': t.uniqueTwist,
  };
  
  let translatedReason = reason;
  Object.entries(reasonMap).forEach(([en, tr]) => {
    translatedReason = translatedReason.replace(en, tr);
  });
  
  return translatedReason.replace('Best for', t.bestFor);
}
